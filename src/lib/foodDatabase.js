// Recherche dans la base locale.
// Match : tous les mots de la query (split sur espace) doivent apparaitre
// dans le nom ou les keywords (insensible aux accents et a la casse).
// Trie pour mettre en premier les noms qui matchent directement.
function normalize(s) {
  return (s || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "") // strip accents
    .replace(/[^a-z0-9 ]/g, " ");
}

export function searchLocalFoods(query) {
  if (!query || query.trim().length < 2) return [];
  const words = normalize(query).split(/\s+/).filter(Boolean);
  if (words.length === 0) return [];

  const results = LOCAL_FOODS.filter((food) => {
    const haystack = normalize(food.name + " " + (food._kw || []).join(" "));
    return words.every((w) => haystack.includes(w));
  });

  // Tri : ceux qui matchent dans le NOM passent en premier, puis ceux qui ne
  // matchent que via les keywords. A egalite, on privilegie les noms courts.
  return results.sort((a, b) => {
    const an = normalize(a.name);
    const bn = normalize(b.name);
    const aDirect = words.every((w) => an.includes(w));
    const bDirect = words.every((w) => bn.includes(w));
    if (aDirect && !bDirect) return -1;
    if (!aDirect && bDirect) return 1;
    return a.name.length - b.name.length;
  });
}
